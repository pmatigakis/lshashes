package com.matigakis.lshashes.hashes.tlsh;

/**
 * Triplet container.
 */
public class Triplet {
    public int c1;
    public int c2;
    public int c3;

    /**
     * Create a new Triplet object.
     *
     * @param c1 the first value
     * @param c2 the second value
     * @param c3 the third value
     */
    public Triplet(int c1, int c2, int c3) {
        this.c1 = c1;
        this.c2 = c2;
        this.c3 = c3;
    }
}
